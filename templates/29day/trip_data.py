def _apply_dates_and_photos():
    di = 0
    for e in S:
        if e[0] == 'day':
            di += 1
            d = DATE_BASE + _dt.timedelta(days=di - 1)
            e[1]['day_label'] = f'DAY {di} · {d.month}.{d.day:02d}'
            if di in TIBET_BG:
                rp = real_photo(TIBET_BG[di])
                if rp:
                    e[1]['bg'] = rp
            elif di in ROUTE_BG:
                rp = ROUTE_BG[di]
                if os.path.exists(rp):
                    e[1]['bg'] = rp
                elif di in SICHUAN_BG:
                    sp = _sil_path(SICHUAN_BG[di])
                    if os.path.exists(sp):
                        e[1]['bg'] = sp
        if e[0] == 'section':
            lbl = e[1].get('label')
            if lbl in SECTION_BG:
                bp = SECTION_BG[lbl]
                if bp and os.path.exists(bp):
                    e[1]['img'] = bp
        elif e[0] == 'closing':
            if os.path.exists(CLOSING_BG):
                e[1]['img'] = CLOSING_BG
        elif e[0] == 'stats':
            if STATS_BG and os.path.exists(STATS_BG):
                e[1]['img'] = STATS_BG
        if e[0] == 'cover':
            end = DATE_BASE + _dt.timedelta(days=28)
            e[1]['meta'] = (f"{DATE_BASE.strftime('%Y.%m.%d')} – {end.strftime('%Y.%m.%d')}"
                            f" · 成都往返 · 约 6200 公里")
            if os.path.exists(ROUTE_COVER):
                e[1]['img'] = ROUTE_COVER
_apply_dates_and_photos()
